//画点 
//x:0~127
//y:0~63
//t:1 填充 0,清空          
void OLED_DrawPoint(uint8 x,uint8 y,uint8 t)
{
  uint8 pos,bx,temp=0;
  if(x>127||y>63)return;//超出范围?
  pos=7-y/8;
  bx=y%8;
  temp=1<<(7-bx);
  if(t)OLED_GRAM[x][pos]|=temp;
  else OLED_GRAM[x][pos]&=~temp;      
}

//x1,y1,x2,y2 填充区域的对角坐?//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63     
//dot:0,清空;1,填充   
void OLED_Fill_1(uint8 x1,uint8 y1,uint8 x2,uint8 y2,uint8 dot)  
{  
  uint8 x,y;  
  for(x=x1;x<=x2;x++)
  {
    for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot);
  }                             
  OLED_Refresh_Gram();//更新显示
}

//更新显存到LCD     
void OLED_Refresh_Gram(void)
{
  uint8 i,n;       
  for(i=0;i<8;i++)  
  { 
    OLED_WrCmd(0xb0+i);
    OLED_WrCmd(0x00);
    OLED_WrCmd(0x10); 
  
    for(n=0;n<128;n++)OLED_WrDat(OLED_GRAM[n][i]); 
  }   
}

